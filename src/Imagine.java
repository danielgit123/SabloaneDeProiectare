
public class Imagine {
	private String nume;

	public Imagine(String nume) {
		super();
		this.nume = nume;
	}

	public String getNume() {
		return nume;
	}

	public void setNume(String nume) {
		this.nume = nume;
	}

	@Override
	public String toString() {
		return "Imagine [nume=" + nume + "]";
	}
	
	
}
